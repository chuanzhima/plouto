public class MerchantAccountInfoLogDto {


    private String creator;

    private Date gmtCreate;

    private String accountId;

    private String source;

    private String type;

    private String description;

    public MerchantAccountInfoLogDto() {

    }

    public MerchantAccountInfoLogDto(String creator, Date gmtCreate, String accountId, String source, String type, String description) {
        this.creator = creator;
        this.gmtCreate = gmtCreate;
        this.accountId = accountId;
        this.source = source;
        this.type = type;
        this.description = description;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public Date getGmtCreate() {
        return gmtCreate;
    }

    public void setGmtCreate(Date gmtCreate) {
        this.gmtCreate = gmtCreate;
    }

    public String getAccountId() {
        return accountId;
    }

    public void setAccountId(String accountId) {
        this.accountId = accountId;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        final StringBuilder sb = new StringBuilder("MerchantAccountInfoLogDto{");
        sb.append("creator='").append(creator).append('\'');
        sb.append(", gmtCreate=").append(gmtCreate);
        sb.append(", accountId='").append(accountId).append('\'');
        sb.append(", source='").append(source).append('\'');
        sb.append(", type='").append(type).append('\'');
        sb.append(", description='").append(description).append('\'');
        sb.append('}');
        return sb.toString();
    }
}
